import { stringify } from 'qs';
import request from '@/utils/request';

const apiBaseUrl = 'https://trackapi.nutritionix.com/v2';

export async function fetchNutrients(params) {
  const body = {
    query: params,
    timezone: 'AU/Sydney',
  };
  return request(`${apiBaseUrl}/natural/nutrients`, {
    method: 'POST',
    credentials: 'omit',
    headers: {
      'Content-Type': 'application/json',
      'x-app-id': 'c38cc0da',
      'x-app-key': '9d14f0732a2535f443f4ce95c1625e7a',
      'x-remote-user-id': 0,
    },
    body: JSON.stringify(body),
  });
}

